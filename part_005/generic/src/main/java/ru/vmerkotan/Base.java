package ru.vmerkotan;

/**
 * Base class.
 *
 * Created by vmerkotan on 1/16/2017.
 */
public abstract class Base {
    /**
     * Returns Id.
     *
     * @return id of the object.
     */
    abstract String getId();

    /**
     * Sets object Id.
     *
     * @param id String id to set.
     */
    abstract void setId(String id);

}
